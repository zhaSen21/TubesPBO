    void isiTextSis() {
        siswa sis = record.get(row);
        nisSiswaField.setText(sis.getNis());
        namaSiswaField.setText(sis.getNama());
        jurusanSiswaField.setText(sis.getJurusan());
        kelasSiswaField.setText(sis.getKelas());
    }

    void kosongkanTextSis() {
        nisSiswaField.setText("");
        namaSiswaField.setText("");
        jurusanSiswaField.setText("");
        kelasSiswaField.setText("");
    }

    void statusAwalSis() {
        kosongkanTextSis();
        loadDataSis();
    }

