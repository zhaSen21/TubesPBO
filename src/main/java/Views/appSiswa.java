    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        String nis = nisSiswaField.getText().toString().trim();
        String nama = namaSiswaField.getText().toString().trim();
        String jurusan = jurusanSiswaField.getText().toString().trim();
        String kelas = kelasSiswaField.getText().toString().trim();

        if (nis.equals("") || nama.equals("") || jurusan.equals("") || kelas.equals("")) {
            JOptionPane.showMessageDialog(null, "Tolong semua data diisi");
        } else {
            try {
                siswa sis = new siswa(null, null, null, null, null, null);
                sis.setNis(nisSiswaField.getText());
                sis.setNama(namaSiswaField.getText());
                sis.setJurusan(jurusanSiswaField.getText());
                sis.setKelas(kelasSiswaField.getText());
                sis.setJumlah("250.000");
                sis.setStatus("Belum Lunas");
                sisServis.insert(sis);
                nisSiswaField.disable();
                namaSiswaField.disable();
                jurusanSiswaField.disable();
                kelasSiswaField.disable();
                nisSiswaDisplay.setText(nisSiswaField.getText());
                namaSiswaDisplay.setText(namaSiswaField.getText());
                jurusanSiswaDisplay.setText(jurusanSiswaField.getText());
                kelasSiswaDisplay.setText(kelasSiswaField.getText());
                submitBtn.disable();

                nisSiswaDisplay1.setText(nisSiswaField.getText());
                namaSiswaDisplay1.setText(namaSiswaField.getText());
                jurusanSiswaDisplay1.setText(jurusanSiswaField.getText());
                kelasSiswaDisplay1.setText(kelasSiswaField.getText());
                tagihanSiswaDisplay.setText("250.000");
                statusSiswaDisplay.setText("Belum Lunas");
                this.statusAwalSis();

                JOptionPane.showMessageDialog(this, "Data Tersimpan");
            } catch (SQLException ex) {
                Logger.getLogger(appAdmin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_submitBtnActionPerformed

