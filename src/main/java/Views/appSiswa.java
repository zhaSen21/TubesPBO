    private void BayarbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BayarbtnActionPerformed
        String nis = nisSiswaDisplay1.getText().toString().trim();
        String nama = namaSiswaDisplay1.getText().toString().trim();
        String jurusan = jurusanSiswaDisplay1.getText().toString().trim();
        String kelas = kelasSiswaDisplay1.getText().toString().trim();
        String jumlah = tagihanSiswaDisplay.getText().toString().trim();
        String status = statusSiswaDisplay.getText().toString().trim();

        if (nis.equals("") || nama.equals("") || jurusan.equals("") || kelas.equals("")) {
            JOptionPane.showMessageDialog(null, "Tidak ada data");
        } else {
            try {
                siswa sis = new siswa(null, null, null, null, null, null);

                nis = nisSiswaDisplay.getText();
                sisServis.delete(nis);

                sis.setNis(nisSiswaDisplay.getText());
                sis.setNama(namaSiswaDisplay.getText());
                sis.setJurusan(jurusanSiswaDisplay.getText());
                sis.setKelas(kelasSiswaDisplay.getText());
                sis.setJumlah("0");
                sis.setStatus("Lunas");
                sisServis.insert(sis);
                nisSiswaDisplay1.setText(nisSiswaDisplay.getText());
                namaSiswaDisplay1.setText(namaSiswaDisplay.getText());
                jurusanSiswaDisplay1.setText(jurusanSiswaDisplay.getText());
                kelasSiswaDisplay1.setText(kelasSiswaDisplay.getText());
                tagihanSiswaDisplay.setText("0");
                statusSiswaDisplay.setText("Lunas");
                this.statusAwalSis();
                JOptionPane.showMessageDialog(this, "Tagihanmu Lunas !");
            } catch (SQLException ex) {
                Logger.getLogger(appAdmin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_BayarbtnActionPerformed

